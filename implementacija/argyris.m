function B = argyris(T,f0,Df0,Hf0)
    V1 = T(1,:); V2 = T(2,:); V3 = T(3,:);
    f = @(x) f0(x(1),x(2));
    Df = @(x) Df0(x(1),x(2));
    Hf = @(x) Hf0(x(1),x(2));
    
    b500 = f(V1); 
    b050 = f(V2); 
    b005 = f(V3);
    
    b410 = f(V1) + Df(V1) * (V2 - V1)' / 5;
    b041 = f(V2) + Df(V2) * (V3 - V2)' / 5;
    b104 = f(V3) + Df(V3) * (V1 - V3)' / 5;
    
    b401 = f(V1) + Df(V1) * (V3 - V1)' / 5;
    b140 = f(V2) + Df(V2) * (V1 - V2)' / 5;
    b014 = f(V3) + Df(V3) * (V2 - V3)' / 5;
    
    b320 = f(V1) + 2/5 * Df(V1) * (V2 - V1)' + 1/20 * (V2 - V1) * Hf(V1) * (V2 - V1)';
    b032 = f(V2) + 2/5 * Df(V2) * (V3 - V2)' + 1/20 * (V3 - V2) * Hf(V2) * (V3 - V2)';
    b203 = f(V3) + 2/5 * Df(V3) * (V1 - V3)' + 1/20 * (V1 - V3) * Hf(V3) * (V1 - V3)';
    
    b311 = f(V1) + 1/5 * Df(V1) * (V2 - V1)' + 1/5 * Df(V1) * (V3 - V1)' + 1/20 * (V3 - V1) * Hf(V1) * (V2 - V1)';
    b131 = f(V2) + 1/5 * Df(V2) * (V3 - V2)' + 1/5 * Df(V2) * (V1 - V2)' + 1/20 * (V1 - V2) * Hf(V2) * (V3 - V2)';
    b113 = f(V3) + 1/5 * Df(V3) * (V1 - V3)' + 1/5 * Df(V3) * (V2 - V3)' + 1/20 * (V2 - V3) * Hf(V3) * (V1 - V3)';
    
    b302 = f(V1) + 2/5 * (Df(V1) * (V3 - V1)') + 1/20 * (V3 - V1) * Hf(V1) * (V3 - V1)';
    b230 = f(V2) + 2/5 * (Df(V2) * (V1 - V2)') + 1/20 * (V1 - V2) * Hf(V2) * (V1 - V2)';
    b023 = f(V3) + 2/5 * (Df(V3) * (V2 - V3)') + 1/20 * (V2 - V3) * Hf(V3) * (V2 - V3)';
    
    N = ones(3,2); TH = zeros(3,3);
    A = [V2 - V1; V3 - V2; V1 - V3];
    for i=1:3
        a = A(i,:);
        if (a(1) ~= 0)
            N(i,1) = - a(2) / a(1);
        else 
            N(i,2) = - a(1) / a(2);
        end
        N(i,:) = N(i,:) / norm(N(i,:));
        TH(i,:) = vectorbary(T, N(i,:));
    end
    
    n = N(1,:); theta = TH(1,:); V12 = (V1 + V2) / 2;
    b221 = 8/15 * 1/theta(3) * Df(V12) * n' + ...
           -1/6 * (b401 + 4*b311 + 4*b131 + b041) + ...
           -theta(1) / (6*theta(3)) * (b500 + 4*b410 + 6*b320 + 4*b230 + b140) + ...
           -theta(2) / (6*theta(3)) * (b050 + 4*b140 + 6*b230 + 4*b320 + b410);
    
    n = N(2,:); theta = TH(2,:); V23 = (V2 + V3) / 2;
    b122 = 8/15 * 1/theta(1) * Df(V23) * n' + ...
           -1/6 * (b140 + 4*b131 + 4*b113 + b104) + ...
           -theta(2) / (6*theta(1)) * (b050 + 4*b041 + 6*b032 + 4*b023 + b014) + ...
           -theta(3) / (6*theta(1)) * (b005 + 4*b014 + 6*b023 + 4*b032 + b041);
    
    n = N(3,:); theta = TH(3,:); V31 = (V3 + V1) / 2;
    b212 = 8/15 * 1/theta(2) * Df(V31) * n' + ...
           -1/6 * (b014 + 4*b113 + 4*b311 + b410) + ...
           -theta(3) / (6*theta(2)) * (b005 + 4*b104 + 6*b203 + 4*b302 + b401) + ...
           -theta(1) / (6*theta(2)) * (b500 + 4*b401 + 6*b302 + 4*b203 + b104);
    
    B = [
        b500 b410 b320 b230 b140 b050;
        b401 b311 b221 b131 b041 nan;
        b302 b212 b122 b032 nan nan;
        b203 b113 b023 nan nan nan;
        b104 b014 nan nan nan nan;
        b005 nan nan nan nan nan;
    ];
    
end
    